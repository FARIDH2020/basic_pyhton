# variabel x dan y menggunakan operator penugasan
x = 15
y = 10

x += y
print("hasil dari x+=y adalah: ", x)

x -= y
print("hasil dari X-=y adalah: ", x)

x *= y
print("hasil dari x*=y adalah: ", x)

x /= y
print("hasil dari x/=y adalah: ", x)

x **= y
print("hasil dari x**=y adalah: ", x)

x //= y
print("hasil dari x//=y adalah: ", x)

x %= y
print("hasil dari x%=y adalah: ", x)
